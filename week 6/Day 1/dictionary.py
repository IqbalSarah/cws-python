"""
Key-value pair
key is always unique, values can repeat
{key:value}
key can be any data type - str,int,bool,float etc
"""

a = {"name": "Sarah Iqbal", "age": 27, "gender": "Female"}
b = {2: "Sarah Iqbal", "adult": True, 10.55: 22.5, False: "Iqbal Sarah"}
print(a)
print(type(a))

print(b)
print(type(b))
